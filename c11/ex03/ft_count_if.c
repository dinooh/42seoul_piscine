int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int res;

	i = 0;
	res = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
			res++;
		i++;
	}
	return (res);
}
