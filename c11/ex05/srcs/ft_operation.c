int		ft_plus(int a, int b)
{
	return (a + b);
}

int		ft_minus(int a, int b)
{
	return (a - b);
}

int		ft_devide(int a, int b)
{
	return (a / b);
}

int		ft_multiply(int a, int b)
{
	return (a * b);
}

int		ft_mod(int a, int b)
{
	return (a % b);
}
