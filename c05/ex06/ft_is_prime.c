int		ft_is_prime(int nb)
{
	int x;

	x = 3;
	if (nb <= 1)
		return (0);
	if (nb <= 3)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0)
		return (0);
	while (x <= nb / 2)
	{
		if (nb % x == 0)
			return (0);
		x += 2;
	}
	return (1);
}
